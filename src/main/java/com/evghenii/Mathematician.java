package com.evghenii;

public class Mathematician {

    public Mathematician() {
    }

    public double sqrt(int zahl) {
        return Math.sqrt(zahl);
    }

    public double abs(int zahl) {
        return Math.abs(zahl);
    }

    public double pow(int zahl, int hoch) {
        return Math.pow(zahl, hoch);
    }
}
